class BasicOperationsScope {
    int addOperationCalc(int operandOne, int operandTwo) {
        int addOperationResult = operandOne + operandTwo;
        return addOperationResult;
    }

    int subtractionOperationCalc(int operandOne, int operandTwo) {
        int addOperationResult = operandOne - operandTwo;
        return addOperationResult;
    }

    int multiplicationOperationCalc(int operandOne, int operandTwo) {
        int addOperationResult = operandOne * operandTwo;
        return addOperationResult;
    }

    int divisionOperationCalc(int operandOne, int operandTwo) {
        int addOperationResult = operandOne / operandTwo;
        return addOperationResult;
    }

    int reminderOperationCalc(int operandOne, int operandTwo) {
        int addOperationResult = operandOne % operandTwo;
        return addOperationResult;
    }
}